#include "../includes/libft.h"

char	*ft_strtoupper(char *s)
{
	int i;

	i = -1;
	if (!s)
		return (NULL);
	while (s[++i] != '\0')
		if (s[i] >= 97 && s[i] <= 122)
			s[i] -= 32;
	return (s);
}
